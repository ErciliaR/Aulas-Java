package Lista02;

import java.text.DecimalFormat;
import java.util.Scanner;

public class Lista02_Q07 
{
	public static void main(String[] args)
	{
		Scanner read = new Scanner(System.in);
		DecimalFormat df = new DecimalFormat("#.0");
		/* variables */
		double base=0, height=0;
		/* input */
		while (base <= 0 || height <=0)
		{
		System.out.println("\nEntre com valores positivos e diferentes de zero.");
		System.out.println("\nEntre com o valor da base do triângulo: ");
		base = read.nextDouble();
		System.out.println("\nEntre com o valor da altura do triângulo: ");
		height = read.nextDouble();
		}
		/* output */
		System.out.println("\nA area do triangulo é: " + df.format(base*height/2) + ".");
	}
}
